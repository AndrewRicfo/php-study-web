            <h1>Архітектура Австралії</h1>
            <p>Архітектура Австралії включає в себе архітектуру Австралійського Союзу та його корінних і колоніальних товариств. Архітектурні стилі колоніального періоду перебували під сильним впливом британської культури. Тим не менш, необхідність адаптації до унікальному клімату Австралії і нові віяння 20-го століття привели зростаючому впливу американського міського дизайну та диверсифікації культурних смаків і вимог все більш мультикультурного австралійського суспільства.
                Серед відомих австралійських архітектурних стилів житлової архітектури - «квінсландер» (англ. Queenslander, приватні міські будинки з дерева на палях, для кращої циркуляції повітря та захисту від термітів), а також «федерація» (англ. Federation architecture, був популярний в 1890 1920 рр.). Серед австралійських об'єктів всесвітньої спадщини ЮНЕСКО - Сіднейський оперний театр, Королівський виставковий центр і Каторжні поселення Австралії.</p>
