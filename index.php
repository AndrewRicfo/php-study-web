        <h1>Театр в Австралії</h1>
        <p>Австралійський театр є важливою частиною австралійської культури. Театр прийшов на континент з першими поселенцями і розвивався завдяки змішанню культур аборигенів та англійців, ввібравши в себе кращі традиції британських та ірландських театрів. Австралійські драматурги і театральні художники мають свій неповторний колорит, відомий в багатьох країнах з тих пір, як в 1788 році культура нового континенту була явлена світу.
            В Австралії розташовуються відомі на весь світ австралійські театральні центри - Сіднейський оперний театр і Національний інститут драматичного іскусстваангл. Серед найяскравіших представників австралійської театральної сцени слід зазначити Сера Роберта Хелпманаангл, Даму Джоан Сазерленд, Баррі Хамфриса і Девіда Вільямсонаангл, Кейт Бланшетт, Джеффрі Раша та Джуді Девіс.
            Неоціненний вплив на австралійський театр зробило корроборі - церемоніальний танець австралійських аборигенів, історія якого сягає далеко в минуле до перших поселенців (за оцінками вчених 40,000-60,000 років тому).</p>
