        <h1 id="m">Мельбурн</h1>
        <img class="exk" src="melbourne.jpg"/>

        <p>Мельбурн - культурна столиця Австралії. В цьому турі Ви подивіться будівлю парламенту штату Вікторія, будівлю
            Королівського виставкового залу, ділову частину Мельбурна, будівлю театру Принцес, проїдете повз Королівського
            ботанічного саду, а також побачите місце проведення австралійського етапу Гран-Прі Формули 1, ринок Королеви
            Вікторії, відомий своєю різноманітністю товарів та продуктів і низькими цінами.
            Ви також насолодитеся красивими видами яхт і моря уздовж набережної Санкт Килда, відвідайте площа Федерації в
            центрі міста, і зробите знімок на тлі центральної станції Фліндерз. Ми також проїдемося вздовж відомих
            спортивних споруд, таких як стадіон MCG, Арена Род Лейвера, та інших цікавих місць. Невелика прогулянка вздовж
            річки Ярра залишить у Вас гарні спогади про Мельбурні. Гід розповість Вам коротку історію Мельбурна і відповість
            на ваші запитання.
            Також, за бажанням, у Вас є можливість піднятися на оглядовий майданчик найвищого житлового будинку в світі -
            хмарочоса Еврика Тауер (оплачується на місці додатково). Швидкісний ліфт підніме Вас на 88 поверх за 39 секунд.
            Звідси, з оглядового майданчика, ви зможете побачити весь Мельбурн, його околиці і Залив Порта Філіпа, і
            сфотографуватися над містом з висоти 300 метрів.</p>

            <p>Тривалість: 4 години</p>

            <p>Вартість: 1-4 людини - $450.00 за групу; 5-11 чоловік - $650.00</p>

