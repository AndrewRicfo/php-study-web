                <h1 id="a">Аделаїда. Острів Кенгуру</h1>
                <img class="exk" src="adelaida.jpg"/>

                <p>Сьогодні нам належить дивовижний, повний пригод день! Їхати далеко, тому почнемо ми дуже рано - ми заберемо вас з
                    готелю в 6:00 ранку. Ми вирушаємо в Кейп Джервіс - мис на півдні Південної Австралії, звідки на поромі
                    перепливемо на острів Кенгуру - райський куточок з неповторною австралійської природою, де в природних умовах,
                    захищені від втручання людини, проживають рідкісні і часто унікальні тварини.
                    Наше пригода починається вже на поромі - дуже часто навколо нього можна побачити пустуючих дельфінів і навіть
                    тюленів.
                    Ми відвідаємо:
                    Seal Bay - заповідник морських левів. Екскурсія проходить на узбережжі, де відпочивають ці шляхетні й спокійні
                    тварини і де ми будемо знаходиться на відстані 2-3 кроків від них.
                    Маленька Сахара - маленька, але справжня пустеля з дюнами, барханами і перекотиполе рослинами.
                    Коала Walk - частина Флиндерс Парку - місце, де можна спостерігати волохатих звіряток в їх самій природній
                    обстановці - сплячих або жують евкаліптові листики.
                    Чудові Скелі і адмірали Arch - два місця, де природа створила неповторні витвори мистецтва з каменю, вітру та
                    води. Також на адмірали Arch проживає колонія волохатих тюленів.</p>

                    <p>Обід і вхідні квитки оплачуються додатково. </p>

                    <p>Вартість: 1-3 людини - $ 1100.00 за групу; 4-11 чоловік - $ 1450.00 </p>
